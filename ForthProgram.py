# formatted string

name = 'Aman'
age = 55

print(f'Hi {name}, you age is {age}')

print('Hi {}, your age is {}'.format(name, age))

# string index

s = '01234567'

print(s[0:2])
print(s[:2])
print(s[0:])
print(s[0:2:1])
print(s[0:2:2])
print(s[:2:2])
print(s[0::2])
print(s[::2])
print(s[-1])
print(s[::-1])  # reverse of string

print(s[::-2])
