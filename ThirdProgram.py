# str (stands for String):-

# 1. use single or double quotes for single line string

print("Hello World")
print('Hello World')

# 2. use 3 single or double quotes for multi line string

print('''
Hello
World
''')

print("""
Hello
World
""")

# string concatination

print("Hello" + " " + "World")

print("Hello " * 5)

# type conversion

print(str(100))
print(type(int(str(100))))

# escape sequence

print("It\'s raining today. ")
